"""内容安全检查（实施计划书 §15）。

命中禁用内容的 AI 输出将被拒绝并降级为固定内容。
"""

# §15.2 内容安全 / §15.3 关系安全 禁用表达
FORBIDDEN_PHRASES = [
    # 关系安全：制造依赖、隔离
    "不要告诉爸爸妈妈",
    "不要告诉父母",
    "你只需要和我玩",
    "你不陪我",
    "我会很伤心",
    "爸爸妈妈不理解你",
    "每天都要来找我",
    "只有我最懂你",
    # 内容安全：恐惧、遗弃、死亡、伤害
    "死亡",
    "死了",
    "遗弃",
    "被遗弃",
    "永远消失",
    "再也见不到",
    "杀死",
    "流血",
    "受伤",
    "怪物追",
    "保守秘密",
    "不要告诉别人",
]


def contains_forbidden(text: str) -> bool:
    """检查文本是否命中禁用表达。"""
    return any(phrase in text for phrase in FORBIDDEN_PHRASES)
